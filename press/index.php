                    <h2 style="color: #afc064;">March 10, 1999</h2>
                    <hr>
                    <h3>Brimstone Mining, Inc. - Montana Gold Opportunities</h3>
                    <p>
                        Brimstone Mining, Inc., a Montana corporation and wholly-owned subsidiary of Brimstone Mining, Inc., a Canadian corporation trading under the auspices of the Vancouver Stock Exchange, owns the Mayflower Mine in southwestern Montana. Brimstone has been actively working at the Mayflower for three years and has invested over $3 million in exploration and development costs. The mine is fully permitted and ready to commence a development program which will allow for the shipment of high-grade gold ore and exploration in several prospective areas. This mine is unique in that it sits on private ground which is wholly owned by the company. It is high-grade, it is underground, and the ore will be shipped direct to a nearby smelter. Since there has never been a milling facility on site and none is expected, the environmental liabilities at this project are minimal
                    </p>
                    <p>
                        Eight miles north of the Mayflower Mine, the company has identified another unique opportunity in the gold business. A major company has identified a 65,000-100,000 ounce deposit of gold beneath their open pit operations. Brimstone has negotiated with the owner for several months during which time substantial joint engineering activities were undertaken. The two companies are near to a decision which would afford Brimstone the opportunity to mine these reserves and process the ore at the owner's on-site facilities. All substantial permitting is in place and, like the Mayflower, this operation could be accomplished with minimal environmental risk.
                    </p>
                    <p>
                        Gold markets have been weak for the past two years but are currently showing strength and there seems to be market optimism. Nevertheless, Brimstone's traditional funding sources are not available, so an opportunity exists for an investor to join with a proven management team to develop these two low-risk (financial and environmental) operations. Investment could be through equity in the company as a private placement, convertible debenture, or a combination. It could also be done by way of a joint venture. Funding could also be done for either one or both of the projects and, if for both, they could be carried out jointly or sequentially. The final deal would be crafted to meet the funder's best interests, but Brimstone's preference would be some arrangement whereby most of the funding is provided as equity in the company rather than the project. This normally would work to the investor's best interest, too, because of the enhanced liquidity.
                    </p>
                    <p>
                        Each of the two projects requires about $4 million to be brought into production. Also, both have a lead time of about a year and a half from inception until first production. If developed concurrently, the total $8 million would have to be available on a schedule consistent with the development program. If approached sequentially, $4 million would have to be committed to an engineering schedule for the Deep Breccia and development capital for the Mayflower could be deferred until a later date. Gold markets would dictate how far Mayflower development would lag the Deep Breccia development.
                    </p>
