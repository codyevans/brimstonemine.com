                    <h2 style="color: #afc064;">August 27, 2004</h2>
                    <hr>
                    <h3>Access to Mayflower Assured</h3>
                    <p>
                        Brimstone Mining, Inc. is pleased to announce that a several yearlong litigation regarding the Company’s access rights to its property via the historic Mayflower Road has been finally resolved in the Company’s favor. In December 2000 a District Court found that Brimstone has a Prescriptive Easement dating back to the 1940’s and that ruling was subsequently upheld by the Montana Supreme Court. The Supreme Court did remand some details back to the District Court but those issues were resolved without further trial and a final stipulation was entered on May 14, 2004. With this final resolution access to the Mayflower Mine has been permanently assured.
                    </p>
                    <p>
                        The Mayflower Mine was discovered in 1896 and quickly became Montana’s richest gold mine. Production continued until 1942 when the War Act caused western gold mines to be shut down. It was reopened from 1958 to 1961. In 1996 Brimstone Mining, Inc. acquired the property and commenced an aggressive program to rehabilitate the entire surface facility and shaft and then begin an exploration-drilling program. Over 20,000 ounces of high grade gold was delineated below the deepest workings of the mine. Several additional targets remain to be explored. The program was suspended because of low gold prices. The facility has been kept in good order and is ready for a renewed program with minimal effort.
                    </p>
                    <p>
                        The Mayflower Mine is free of debt, has clear title to fee simple property, is fully and modernly equipped, is permitted (minor updating required), and could be up and running in 4-5 weeks. 
                    </p>
